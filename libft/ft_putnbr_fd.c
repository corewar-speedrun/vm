#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long int	p;
	char		x;

	p = n;
	if (p < 0)
	{
		write(fd, "-", 1);
		p = p * -1;
	}
	if (p > 9)
	{
		ft_putnbr_fd(p / 10, fd);
	}
	x = p % 10 + 48;
	write(fd, &x, 1);
}
