#include "libft.h"

void	ft_putnbr(int n)
{
	long int	p;
	char		x;

	p = n;
	if (p < 0)
	{
		write(1, "-", 1);
		p = p * -1;
	}
	if (p > 9)
	{
		ft_putnbr(p / 10);
	}
	x = p % 10 + 48;
	write(1, &x, 1);
}
